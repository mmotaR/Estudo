# Operadores Matemáticos

# Operações básicas: +, -, *, /, **, //, %
# % Resto da divisão ! Ex. 10 % 3 = 1 ou 4 % 2 = 0
# ** elevado ! Ex. 2 ** 3 = 8 ou 5 ** 2 = 25 (potenciação)

# Operadores de comparação: ==, !=, >, <, >=, <=
# == Igual a ! Ex. 5 == 5 é True ou 3 == 4 é False
# != Diferente de ! Ex. 5 != 4 é True ou 3 != 3 é False
# > Maior que ! Ex. 5 > 3 é True ou 2 > 4 é False
# < Menor que ! Ex. 3 < 5 é True ou 4 < 2 é False
# >= Maior ou igual a ! Ex. 5 >= 5 é True ou 4 >= 5 é False
# <= Menor ou igual a ! Ex. 3 <= 4 é True ou 5 <= 4 é False 

print(2 + 3)  # Soma
print(5 - 2)  # Subtração
print(4 * 3)  # Multiplicação   
print(10 / 2)  # Divisão
print(2 ** 3)  # Potenciação    
print(10 % 3)  # Resto da divisão
print(10 // 3)  # Divisão inteira
print(5 == 5)  # Igual a
print(3 != 4)  # Diferente de
print(5 > 3)  # Maior que
print(3 < 5)  # Menor que
print(5 >= 5)  # Maior ou igual a
print(3 <= 4)  # Menor ou igual a
